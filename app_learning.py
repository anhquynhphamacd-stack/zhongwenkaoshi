def ai_grade_essay(question, student_answer, reference_answer):
    client = get_ai_client()
    if not client:
        return {"score": 0.0, "comment": "❌ 系統未設置正確的 API Key。此為主觀題，請參考下方標準答案自行對照學習。"}
    
    prompt = f"""
    你是一位專業的對外漢語（華語）教師。請針對學生的中文（繁體中文）回答進行精確、友善的批改。
    
    【題目】: {question}
    【參考標準答案】: {reference_answer}
    【學生提交的答案】: {student_answer}
    
    請從以下兩個維度評分（滿分10分）：
    1. 語意正確度 (5分): 是否符合題目要求，意思是否表達正確。
    2. 語法與錯別字 (5分): 漢字是否正確（優先支持正體/繁體字），句型結構是否使用正確。
    
    請嚴格以下列 JSON 格式輸出回饋，不要包含任何額外的 Markdown 標記或 prose：
    {{
        "score": 總分(0.0到10.0之間的浮點數),
        "comment": "繁體中文的具體評語。如果扣分，請指出學生的語法錯誤或錯別字，並給出修改建議(正确写法)；如果滿分，請給予鼓勵。"
    }}
    """
    
    try:
        response = client.models.generate_content(
            model='gemini-2.5-flash',
            contents=prompt,
            config=types.GenerateContentConfig(
                response_mime_type="application/json",
                temperature=0.2,
            ),
        )
        result = json.loads(response.text.strip())
        return result
    except Exception as e:
        return {"score": 0.0, "comment": f"⚠️ AI 批改出錯: {str(e)}"}

